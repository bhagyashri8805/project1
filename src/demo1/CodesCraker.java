package demo1;
public class CodesCraker
{
   public static void main(String[] args)
   {
      int a = 20, b = 15, c;
      
      c = a + b;
      System.out.println("Add = " + c);
      c = a - b;
      System.out.println("Sub = " + c);
      c = a * b;
      System.out.println("Mul = " + c);
      c = a / b;
      System.out.println("Div = " + c);
   }
}